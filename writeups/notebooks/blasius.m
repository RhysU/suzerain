                     (blasius_eta.*blasius_f.*blasius_fpp)./2))   ...
                     + blasius_fpp.**2.*(blasius_eta.**2 + 4.*Re) ...
                     + blasius_fp.*(blasius_eta.*blasius_fpp -    ...
                     (blasius_f.*blasius_fpp.*(blasius_eta.**2 +  ...
